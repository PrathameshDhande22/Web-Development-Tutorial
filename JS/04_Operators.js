let a = 4, b = 3;

// Arithmetic operators
console.log(a + b);
console.log(a - b)
console.log(a * b)
console.log(a++)
console.log(a)
// expontentiation operator
console.log(a ** b)
//modulo operator return the remainder of the number
console.log(a % b)

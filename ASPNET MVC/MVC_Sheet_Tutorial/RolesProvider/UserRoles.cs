        public override string[] GetRolesForUser(string username)
        {
            MVCApplication db = new MVCApplication();
            var userroles = (from login in db.LoginDetails
                             join mapping in db.RoleMappings on login.Id equals mapping.UserLogin.Id
                             where login.Username == username
                             select mapping.RoleId.Rolename.ToString()).ToArray();

            return userroles;
        }

